/* 
Una parte importante del desarrollo de SW es la corrección de los errores que siempre va estar ahí y es 
imposible de evitar, en la correcion de errores es indispensable identificarlos lo mas pronto posible y 
para eso existen muchas formas de encontrar estos problemas en el código pero hay una que nunca debe dejarse de lado
y son las pruebas:

Pruebas manuales: estas pruebas son realizadas por personas que están probando el nuevo sistema lo cual consume 
tiempo y esfuerzo, costoso y poco confiable.


Pruebas automatizadas es decir escribir programas que sean capaces de probar los programas y reportar los fallos, 
esto es mas rápido, menos costoso y mas confiable.

Pero ahí cosas que no se pueden testiar en forma automatizada como por ejemplo ¿es facil de manejar?, 
¿tiene buen diseño? esto se tiene que realizar de manera manual. 

Para testing automatizado se tienen los testing unitarios y testing de integración. 

Testing unitarios tomamos cada unidad de SW y validarlo por separado para ver si hace lo que tiene que hacer, 
esto nos ayuda a refactorizar, tener mas estructurado el código, fácil al integración con otras partes y documentado.
 
Testing de integración, es el complemento de test unitarios, es decir validar la interacción entre los diferentes 
componentes de del SW. Si tenemos buenas pruebas unitarias también debemos re realizar unas pruebas de itegracion.





